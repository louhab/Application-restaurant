    /**
     * Store a newly created resource in storage.
     *
     * @param  \Illuminate\Http\Request  $request
     * @return \Illuminate\Http\Response
     */
    public function store(Request $request)
    {
       // dd($request->all());
       $sales =new Sales;
       $sales->servant_id=$request->servant_id;
       $sales->quentity=$request->qte;
       $sales->totale_price=$request->total_price;
       $sales->totale_reseved=$request->total_received;
       $sales->change=$request->change;
       $sales->type_payement=$request->payment_type;
       $sales->payement_statu=$request->payment_statu;
       $sales->save();
       $sales->menus()->sync($request->menu_id);
       $sales->tables()->sync($request->table_id);
       return redirect()->route("payement.index")->with([
        'success'=>" la creation a ete faite !"
    ]);
      
    }

